public class DualEquation {

    private double a_param = 0;
    private double b_param = 0;
    private double c_param = 0;

    public double getA_param(){
        return a_param;
    }

    public double getB_param(){
        return b_param;
    }

    public double getC_param(){
        return c_param;
    }

    public void dualequation(double a_param, double b_param, double c_param){
        this.a_param = a_param;
        this.b_param = b_param;
        this.c_param = c_param;
    }

}
